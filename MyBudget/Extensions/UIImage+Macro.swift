import Foundation

extension UIImage {
    class func bagImage() -> UIImage {
        return UIImage.init(named: "Bag")!
    }
    
    class func creditcardsImage() -> UIImage {
        return UIImage.init(named: "Credit Cards")!
    }
    
}
